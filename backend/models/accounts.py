from sqlalchemy import (
    Column, Integer, String, Text, Boolean,
    Numeric, Date, DateTime, ForeignKey, func, Computed
)
from sqlalchemy.orm import relationship
from database import Base

class AdvancePayment(Base):
    __tablename__ = "advance_payments"
    adv_id          = Column(Integer, primary_key=True)
    fy_code         = Column(String(10), ForeignKey("financial_years.fy_code"), nullable=False, index=True)
    voucher_no      = Column(String(30), unique=True, nullable=False)
    voucher_date    = Column(Date, nullable=False)
    gl_party_id     = Column(Integer, ForeignKey("gl_master.gl_id"), nullable=False, index=True)
    party_name      = Column(String(200), nullable=False)
    party_type      = Column(String(10), nullable=False, default='Supplier')
    gl_cashbank_id  = Column(Integer, ForeignKey("gl_master.gl_id"), nullable=False)
    cashbank_name   = Column(String(200))
    amount          = Column(Numeric(12,2), nullable=False)
    adjusted_amount = Column(Numeric(12,2), nullable=False, default=0)
    balance         = Column(Numeric(12,2), Computed("amount - adjusted_amount"))
    doc_no          = Column(String(50))
    doc_date        = Column(Date)
    narration       = Column(Text)
    status          = Column(String(20), nullable=False, default='Open', index=True)
    created_by      = Column(Integer, ForeignKey("users.user_id"))
    created_at      = Column(DateTime, default=func.now(), nullable=False)

