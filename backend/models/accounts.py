class ReceiptVoucher(Base):
    __tablename__ = "receipt_vouchers"
    receipt_id      = Column(Integer, primary_key=True)
    fy_code         = Column(String(10), ForeignKey("financial_years.fy_code"), nullable=False)
    receipt_no      = Column(String(30), unique=True, nullable=False)
    receipt_date    = Column(Date, nullable=False)
    owner_id        = Column(Integer, ForeignKey("pet_owners.owner_id"), nullable=False)
    gl_party_id     = Column(Integer, ForeignKey("gl_master.gl_id"), nullable=False)
